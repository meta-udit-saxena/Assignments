package com.metacube.roomallotment;

/**
 * The Class Guest.
 */
public class Guest {

	/** The name. */
	String name;

	/** The room. */
	int age, room;

	/**
	 * Gets the name.
	 *
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * Sets the name.
	 *
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * Gets the age.
	 *
	 * @return the age
	 */
	public int getAge() {
		return age;
	}

	/**
	 * Sets the age.
	 *
	 * @param age
	 *            the age to set
	 */
	public void setAge(int age) {
		this.age = age;
	}

	/**
	 * Gets the room.
	 *
	 * @return the room
	 */
	public int getRoom() {
		return room;
	}

	/**
	 * Sets the room.
	 *
	 * @param room
	 *            the room to set
	 */
	public void setRoom(int room) {
		this.room = room;
	}
}
